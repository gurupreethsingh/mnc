package varibles_g;

public class Varibles 
{
   public static void main(String[] args) 
   {
	 // data types and varibles. 
	   System.out.println(10);
	   System.out.println(2.35);
	   
	   // character literals , single quotes. 
	   System.out.println('a');
	   System.out.println('9');
	   
	   // string literals 
	   System.out.println("hello");
	   System.out.println("96987686");
	   System.out.println("abc123");
	   System.out.println("          ");
	   
	   // boolean 
	   System.out.println(true);
	   System.out.println(false);
	   
	   // 8 data types 
	   // declaring varaibles.
	   byte a1 = 1;
	   short s = 2;
	   char ch = 'a';
	   int a = 100;
	   float f = 2.35f;
	   long l = 46546465469798798l;
	   double d = 5646546.654654654;
	   //String , inbuilt class in java. 
	   String s1 = "chetan";
	   String s2 = "Nutan";
	   String s3 = "Gurupeeth";
	   String username = "chatan";
	   
	   System.out.println(a1);
	   System.out.println(s);
	   System.out.println(ch);
	   System.out.println(a);
	   System.out.println(f);
	   System.out.println(l);
	   System.out.println(d);
	   System.out.println(s1);
	   System.out.println(s2);
	   System.out.println(s3);
	   System.out.println(username);
	   
   }
}
