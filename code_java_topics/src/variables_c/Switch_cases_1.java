package variables_c;

public class Switch_cases_1 {

	public static void main(String[] args) {
		String un = "chetan";
		switch (un)
		{
			case "chetan":
			{
				System.out.println("Access granted");
			}
		break;
			case "ram":
			{
				System.out.println("Access granted");
			}
		break;
			case "sham":
			{
				System.out.println("Access granted");
			}
		break;
		default :
		{
			System.out.println("Access denied");
		}
		}
	
	}

}
