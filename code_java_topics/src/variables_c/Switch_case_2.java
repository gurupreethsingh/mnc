public class Switch_case_2 {

	public static void main(String[] args) {
		String un = "sham";
		switch (un)
		{
			case "chetan":
			{
				System.out.println("Access granted");
			}
		break;
			case "ram":
			{
				System.out.println("Access granted");
			}
		break;
			case "sham":
			{
				System.out.println("Access granted");
			}
		break;
		default :
		{
			System.out.println("Access denied");
		}
	}

	}
}
